// Arrow functions

// Se agregó en ECMAScript 6.
// Se declaran como una function expression (como una variable)
// Las arrow functions permiten simplificar el código
// Cuando las arrow function tienen solo una línea, dan por implícito el "return"
// Las llaves son opcionales. Se utilizan para múltiples lineas en una arrow function.

const aprendiendo = function(){
  console.log("Aprendiendo JS");
}
aprendiendo();

// ARROW FUNCTION
const aprendiendo2 = () => "Aprendiendo Arrow Functions";
console.log(aprendiendo2());