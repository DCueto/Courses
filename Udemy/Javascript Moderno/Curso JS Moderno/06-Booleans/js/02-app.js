const boolean1 = true;
const boolean2 = false;

console.log(boolean1 === boolean2); // Devuelve false. Compara tipo de objeto y valor.
// Tipo de objeto es el mismo, pero valor es diferente.

console.log(boolean1 === true); // Devuelve true. Compara tipo de objeto y valor.
// Tipo de objeto es el mismo y valor es el mismo.